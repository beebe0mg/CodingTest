import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String str; 
        int n;
        str = sc.next();
        n = sc.nextInt();
        for(int i=0; i<n; i++) {
            System.out.print(str);
        }
        
    }
}